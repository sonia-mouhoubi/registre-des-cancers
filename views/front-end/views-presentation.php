            <h2>Qu’est-ce qu’un registre ?</h2> 

            <p>Un registre est une structure qui met en œuvre un recueil continu et exhaustif de données nominatives, intéressant un ou plusieurs évènements de santé dans une population géographiquement définie, à des fins de surveillance épidémiologique et de recherche.</p>

            <p>Les registres des cancers en France sont organisés en un réseau français des registres de cancers FRANCIM (France-Incidence-Mortalité). Ce réseau constitue l’acteur principal de la surveillance épidémiologique des cancers en France, en collaboration avec l’Institut National du Cancer (InCA https://www.e-cancer.fr/ ), l’Institut Santé publique France (SpF https://www.santepubliquefrance.fr/ ) et les Hospices civils de Lyon ( https://www.chu-lyon.fr/ ).</p>
            
            <img src="assets/img/carte-cancer-france-2019.jpg" alt="Image Registres des cancers en 2019">

            <h3>En Haute-Vienne</h3> 

            <p>Le registre des cancers de la Haute-Vienne a été créé en 1998. Il est situé au Centre Hospitalier Universitaire (CHU) de Limoges, dont il constitue une unité fonctionnelle. Le registre couvre le département de la Haute-Vienne, soit une population estimée au 01/01/2019 à 372 359 habitants(INSEE).</p>

            <p>Le registre des cancers de la Haute-Vienne est un registre général. Il prend en compte tous les cas survenant chez des personnes résidant dans le département quels que soient leur âge, leur sexe, leur lieu de prise en charge.</p>

            <h3>Base juridique et responsable du traitement de vos données personnelles</h3>

            <p>Les données à caractère personnel collectées par les registres font l’objet d’un traitement informatique réalisé sous la responsabilité du CHU de Limoges, auquel le registre est rattaché. Ce traitement est nécessaire à l’exécution de la mission de service public des registres. Cette mission est au cœur de la politique de lutte contre le cancer affirmée par les pouvoirs publics comme une priorité nationale en matière de santé publique (Article L. 1413-3 et L. 1413-6 du code de la santé publique). Les registres ont une autorisation de la Commission Nationale de l’Informatique et des Libertés (CNIL) et sont qualifiés par le Comité d’Évaluation des Registres.</p>  
    </section>

    <section>
        <h2>Méthodes</h2>
        <h3>Cas enregistrés</h3>

        <p>Tous les nouveaux de cancers doivent être enregistrés dont :</p>
            <ul>
                <li>Les hémopathies malignes</li>
                <li>Les tumeurs solides invasives</li>
                <li>les tumeurs in situ</li>
                <li>les tumeurs borderline des ovaires</li>
                <li>les tumeurs bénignes ou d’évolution imprévisible du système nerveux central et de la vessie.</li>
            </ul>
            
        <p>Les carcinomes épidermoïdes de la peau sont également enregistrés mais leur exhaustivité n’est pas garantie. Les carcinomes basocellulaires de la peau ne sont pas enregistrés par le registre. Les récidives et les métastases d’une autre tumeur ne sont pas prises en compte.</p>

        <p>Les récidives et les métastases d’une autre tumeur ne sont pas prises en compte.</p>

        <h3>Durée de l’étude</h3>

        <p>L’enregistrement des données est continu, et constitue l’activité principale du Registre. Les notifications des cas de cancers ainsi que la validation des données se font suivant un calendrier bien défini, réparti sur une année.</p>
    </section>
            
    <section>
        <h2>Fonctionnement général</h2>

        <h3>Sources des données</h3>

        <p>Pour effectuer le recensement des cas et être exhaustif, le registre collabore avec les établissements autorisé à assurer la prise en charge de la maladie (voir https://www.e-cancer.fr/Professionnels-de- sante/L-organisation-de-l-offre-de-soins/Traitements-du-cancer-les-etablissements-autorises ). Différents types de données sont collectées et proviennent de nombreuses sources. Elles sont
        constituées essentiellement de données à caractère personnel administratives et de données de santé. Les informations collectées par les registres des cancers sont nominatives et ce critère est indispensable pour garantir qu’un cas identifié ne soit pas compté en double et que les estimations des indicateurs soient le reflet exact de la réalité observé.</p>

        <p>Les principales sources d’information du registre sont :</p>

        <ul>
            <li>les laboratoires d’anatomie et de cytologie pathologiques (ACP) publics et privés de la Haute-Vienne,</li>
            <li>les Départements d&#39;Information Médicale (DIM, PMSI) des établissements publics et privés,</li>
            <li>les Caisses d’Assurance Maladie,</li>
            <li>Le réseau régional de cancérologie de la Nouvelle Aquitaine (ONCO-NA https://onco-
        nouvelle-aquitaine.fr/ ),</li>
            <li>Et les laboratoires d’hématologie, de biologie moléculaire et de cytogénétique.</li>
        </ul>

        <p>Les informations transmises par les sources sont ensuite vérifiées par le personnel du registre de façon scrupuleuse et minutieuse. Lorsqu’un cas est confirmé, il est enregistré dans la base du registre.</p>
    </section>

    <section>
        <h2>Quelles sont les informations recueillies ?</h2>

        <p>Les registre recueille des données d’identification (identité des patients, adresse) et médicales (date de diagnostic, caractéristiques des cancers/tumeurs, types de traitement, évolution de la maladie).</p>

        <p>Ces données permettent notamment de connaître le type et la fréquence des cancers dans la population et leur évolution dans le temps. Elles sont indispensables pour améliorer les connaissances sur les causes de cancer et évaluer la qualité de la prise en charge.</p>
    </section>

    <section>
        <h2>Protection et sécurité</h2>

        <p>Les données recueillies sont couvertes par le secret médical et sont strictement confidentielles. Seuls les personnels du registre dûment habilités ont un accès aux données nominatives et le traitement des données est réalisé de manière à respecter la confidentialité conformément à la réglementation en vigueur (Loi « informatique et libertés » modifiée et Règlement Général pour la Protection des Données, RGPD). Les données des registres présentant un intérêt historique, scientifique ou statistique, elles sont conservées le temps nécessaire à l’accomplissement de leurs missions. Seules les données totalement anonymes font l’objet de publications.</p>
    </section>

    <section>
        <h2>Quels sont les destinataires de données ?</h2>

        <p>Chaque année, les données ainsi collectées sont transmises à la base commune des registres de cancers sous un format non nominatif pour estimer différents indicateurs au niveau départemental, régional, national et même international (taux d’incidence, taux de mortalité, survie); elles servent également à différentes types d’études.</p>

        <p>Certaines données (à l’exception des données relatives à votre identité) pourront également, dans des conditions assurant leur confidentialité, être transmises à d’autres organismes (base commune des registres de cancer du Réseau Francim, Institut National du Cancer [INCa], notamment la plateforme de données en cancérologie, Santé publique France [SpF], Centre International de Recherche sur le Cancer [CIRC]) pour permettre par exemple des comparaisons géographiques des cancers en France et dans le monde.</p>
    </section>

